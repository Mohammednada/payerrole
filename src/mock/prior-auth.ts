/* ------------------------------------------------------------------ */
/*  Eligibility check simulation                                       */
/* ------------------------------------------------------------------ */

export function simulateEligibilityCheck(paType: PaType): PaEligibilityCheck {
  const benefitMap: Record<PaType, string> = {
    inpatient: 'Inpatient Hospital',
    outpatient: 'Outpatient Surgery',
    pharmacy: 'Prescription Drugs (Brand)',
    imaging: 'Imaging/Radiology',
    DME: 'Durable Medical Equipment',
    'home-health': 'Home Health Care',
  };

  const category = benefitMap[paType];
  const benefit = mockEligibilityResult.benefits.find((b) => b.category === category);

  return {
    checked: true,
    eligible: mockEligibilityResult.coverageStatus === 'active',
    coverageStatus: mockEligibilityResult.coverageStatus,
    planName: mockEligibilityResult.planName,
    authRequired: benefit?.authRequired ?? true,
    benefitNote: benefit?.notes ?? `In-Network: ${benefit?.inNetwork ?? 'N/A'}`,
    deductibleMet: mockEligibilityResult.deductible.individualMet,
    deductibleMax: mockEligibilityResult.deductible.individual,
  };
}

